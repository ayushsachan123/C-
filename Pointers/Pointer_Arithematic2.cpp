#include<iostream>
using namespace std;
int main(){
    int arr1[5]={2,4,6,8,10};
    int arr2[5]={1,2,3,4,5};
    int *p;
    p=arr1;
    for(int i=0;i<5;i++){
        cout<<*(p+i)<<endl;
    }
    int *q;
    q=arr2;

    for(int i=0;i<5;i++){
        cout<<q[i]<<endl;
    }


    return 0;
}

